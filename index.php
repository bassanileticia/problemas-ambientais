            <h2>Sobre o Projeto</h2>

            <p>
                O EcoDenúncia é um sistema desenvolvido com o objetivo de auxiliar
                na identificação e no registro de problemas ambientais urbanos.
                Atualmente, muitas cidades enfrentam dificuldades relacionadas ao
                descarte irregular de lixo, queimadas, poluição de rios,
                entupimento de bueiros e acúmulo de resíduos em terrenos baldios.
                Esses problemas afetam diretamente a qualidade de vida da população,
                além de causarem impactos negativos ao meio ambiente.
            </p>

            <p>
                Muitas vezes, os moradores percebem esses problemas diariamente,
                porém não possuem uma forma simples e organizada de registrar
                ocorrências ou compartilhar informações importantes sobre a situação.
                Pensando nisso, o EcoDenúncia foi criado como uma ferramenta digital
                que permite registrar denúncias ambientais de maneira rápida,
                prática e acessível.
            </p>

        </section>

        <section>

            <h2>Problema Ambiental</h2>

            <p>
                O descarte irregular de lixo é um dos problemas ambientais mais
                comuns em áreas urbanas. Em diversos bairros, é possível encontrar
                terrenos baldios com acúmulo de entulho, sacolas plásticas,
                restos de móveis, pneus e outros materiais descartados de forma
                inadequada.
            </p>

            <p>
                Além da poluição visual, o lixo acumulado pode provocar mau cheiro,
                atrair insetos e animais transmissores de doenças, além de aumentar
                os riscos de enchentes devido ao entupimento de bueiros e sistemas
                de drenagem.
            </p>

            <p>
                Outro problema importante é a falta de conscientização ambiental.
                Muitas pessoas ainda descartam resíduos em locais inadequados,
                sem considerar os impactos que isso pode causar à natureza e à
                saúde pública.
            </p>

        </section>

